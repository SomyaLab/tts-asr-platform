// Helper function to get voice data by language and gender
export const getVoiceData = (language, gender) => {
  return VOICE_METADATA[language]?.[gender] || null
}

// Helper function to get voice data by voiceId
export const getVoiceDataById = (voiceId) => {
  for (const lang in VOICE_METADATA) {
    for (const gender in VOICE_METADATA[lang]) {
      if (VOICE_METADATA[lang][gender].voiceId === voiceId) {
        return VOICE_METADATA[lang][gender]
      }
    }
  }
  return null
}

// Helper function to get all voices
export const getAllVoices = () => {
  const voices = []
  for (const lang in VOICE_METADATA) {
    for (const gender in VOICE_METADATA[lang]) {
      voices.push(VOICE_METADATA[lang][gender])
    }
  }
  return voices
}

